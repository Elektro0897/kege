num = 15
# id() - выдаёт номер ячейки в памяти, где хранится переменная
print(id(num))

# Целое число / integer / int
num2 = 20
print(type(num2))

# Дробные числа / вещественные числа / числа с плавающей точкой /float
fl_num = 3.14
print(type(fl_num))

# строка / string / str
str1 = 'str1'
str2 = "str2"
print(type(str1))

# список / list
lt = [3, 'hi', 5.6225]
print(type(lt))

# кортеж / tuple
tp = (4, 6, 'pr', 5.5)
print(type(tp))

# множество / set
st = {5, 7, 5, 3, 5, 6, 9, 7, 8}
print(type(st))

# словарь / dictionary / dict
client = {'name': 'oleg', 'height': 230, 'age': 23}
print(type(client))

# логический тип / булевый тип / boolean / bool
logic = True
print(type(logic))
